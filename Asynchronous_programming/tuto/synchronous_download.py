import sys
import time

from Asynchronous_programming.tuto.download import collect_links, create_download_dir, download_link


def update_progress(progress, operation_type):
    barLength = 100  # Modify this to change the length of the progress bar
    status = ""
    if isinstance(progress, int):
        progress = float(progress)
    if not isinstance(progress, float):
        progress = 0
        status = "error: progress var must be float\r\n"
    if progress < 0:
        progress = 0
        status = "Halt...\r\n"
    if progress >= 1:
        progress = 1
        status = "Done...\r\n"
    block = int(round(barLength * progress))
    text = "\r {0} Download |---> [{1}] Percent: {2}% {3}".format(operation_type,
                                                                  "#" * block + "-" * (barLength - block),
                                                                  progress * 100, status)
    sys.stdout.write(text)
    sys.stdout.flush()


def main():
    ts = time.time()
    website_1 = "http://icecat.biz/p/toshiba/pscbxe-01t00een/satellite-pro-notebooks-4051528049077-Satellite+Pro+C8501GR-17732197.html"
    website_2 = "https://unsplash.com/search/photos/hd"

    download_dir = create_download_dir()
    links = [*collect_links(website_2), *collect_links(website_1)]
    progress = 0

    for link in links:
        download_link(download_dir, link)
        progress = progress + 1
        update_progress(progress / len(links), "Synchronous")

    info = 'Took {} seconds to download {} files'.format(time.time() - ts, len(links))
    sys.stdout.write("]\n " + info)


if __name__ == '__main__':
    main()
