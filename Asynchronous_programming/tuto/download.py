def collect_links(website):
    website = urlopen(website)
    soup = BeautifulSoup(website, 'html.parser')
    try:
        return list(
            filter(None, [image.get('src') for image in soup.findAll('img') if validators.url(image.get('src'))]))
    except TypeError:
        return list(filter(None, [image.get('src') for image in soup.findAll('img')]))


def download_link(directory, link):
    download_path = directory / os.path.basename(link)
    urlretrieve(link, download_path)
    # with urlopen(link) as image, download_path.open('wb') as f:
    #     f.write(image.read())
    logger.info('Downloaded %s', link)


def create_download_dir():
    download_dir = Path('images')
    if not download_dir.exists():
        download_dir.mkdir()
    return download_dir
